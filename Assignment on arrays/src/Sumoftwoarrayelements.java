
public class Sumoftwoarrayelements
{

	public static void main(String[] args) 
	{
		int sum ;
		int array1[] = {10, 20, 30, 40, 50};
		int array2[] = {9, 18, 27, 36, 45};
		System.out.print("sum of two arrays:");
		for (int i=0; i<5; i++)
		{
			sum = array1[i]+array2[i];
			System.out.print(" "+ sum );
		}
	} 
	}
